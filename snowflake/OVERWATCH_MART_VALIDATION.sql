-- 9) Reconciliation configuration and last run proof.
WITH latest_runs AS (
    SELECT
        r.*,
        ROW_NUMBER() OVER (
            PARTITION BY r.CHECK_ID
            ORDER BY r.RUN_TS DESC, r.RUN_ID DESC
        ) AS rn
    FROM OVERWATCH_RECON_RUN r
)
SELECT
    c.CHECK_ID,
    c.CHECK_NAME,
    c.SOURCE_DATABASE,
    c.SOURCE_SCHEMA,
    c.TARGET_DATABASE,
    c.TARGET_SCHEMA,
    c.TABLE_PATTERN,
    c.CHECK_MODE,
    c.SEVERITY,
    c.OWNER,
    c.ENABLED,
    lr.RUN_TS AS LAST_RUN_TS,
    lr.RUN_STATUS AS LAST_RUN_STATUS
FROM OVERWATCH_RECON_CONFIG c
LEFT JOIN latest_runs lr
  ON lr.CHECK_ID = c.CHECK_ID
 AND lr.rn = 1
ORDER BY c.ENABLED DESC, c.SEVERITY, c.CHECK_NAME;

-- 10) Secure-view and dynamic-table compatibility guard.
SHOW DYNAMIC TABLES IN SCHEMA;

WITH dynamic_objects AS (
    SELECT UPPER("name") AS OBJECT_NAME
    FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()))
),
overwatch_dynamic_collisions AS (
    SELECT OBJECT_NAME
    FROM dynamic_objects
    WHERE OBJECT_NAME ILIKE 'FACT\_%' ESCAPE '\'
       OR OBJECT_NAME ILIKE 'DIM\_%' ESCAPE '\'
       OR OBJECT_NAME ILIKE 'MART\_%' ESCAPE '\'
       OR OBJECT_NAME ILIKE 'OVERWATCH\_%' ESCAPE '\'
       OR OBJECT_NAME ILIKE 'ALERT\_%' ESCAPE '\'
)
SELECT
    'DYNAMIC_TABLE_COLLISIONS' AS CHECK_NAME,
    COUNT(*) AS ISSUE_COUNT,
    IFF(COUNT(*) = 0, 'PASS', 'FAIL') AS VALIDATION_STATUS,
    LISTAGG(OBJECT_NAME, ', ') WITHIN GROUP (ORDER BY OBJECT_NAME) AS DETAILS
FROM overwatch_dynamic_collisions;

SELECT
    'SECURE_VIEW_COLLISIONS' AS CHECK_NAME,
    COUNT(*) AS ISSUE_COUNT,
    IFF(COUNT(*) = 0, 'PASS', 'FAIL') AS VALIDATION_STATUS,
    LISTAGG(TABLE_NAME, ', ') WITHIN GROUP (ORDER BY TABLE_NAME) AS DETAILS
FROM INFORMATION_SCHEMA.VIEWS
WHERE TABLE_SCHEMA = CURRENT_SCHEMA()
  AND UPPER(COALESCE(IS_SECURE, 'NO')) = 'YES'
  AND (
      TABLE_NAME ILIKE 'FACT\_%' ESCAPE '\'
      OR TABLE_NAME ILIKE 'DIM\_%' ESCAPE '\'
      OR TABLE_NAME ILIKE 'MART\_%' ESCAPE '\'
      OR TABLE_NAME ILIKE 'OVERWATCH\_%' ESCAPE '\'
      OR TABLE_NAME ILIKE 'ALERT\_%' ESCAPE '\'
  );

