-- OVERWATCH benchmark report SQL
-- Run after the local runner and any Snowflake synthetic generation.

USE DATABASE DBA_MAINT_DB;
USE SCHEMA OVERWATCH;

ALTER TABLE OVERWATCH_USAGE_LOG ADD COLUMN IF NOT EXISTS PERF_RUN_ID VARCHAR(100);

CREATE OR REPLACE VIEW PERF_TEST_APP_USAGE_REPORT_V AS
SELECT
    COALESCE(PERF_RUN_ID, 'UNLABELED') AS PERF_RUN_ID,
    SECTION,
    EVENT_TYPE,
    COUNT(*) AS EVENTS,
    ROUND(AVG(QUERY_DURATION_MS), 0) AS AVG_DURATION_MS,
    ROUND(PERCENTILE_CONT(0.95) WITHIN GROUP (ORDER BY QUERY_DURATION_MS), 0) AS P95_DURATION_MS,
    MAX(QUERY_DURATION_MS) AS MAX_DURATION_MS,
    SUM(COALESCE(ROW_COUNT, 0)) AS ROWS_RETURNED,
    ROUND(SUM(COALESCE(RESULT_MB, 0)), 2) AS RESULT_MB,
    SUM(IFF(COALESCE(USED_CACHE, FALSE), 1, 0)) AS CACHE_HITS,
    SUM(IFF(NOT COALESCE(USED_CACHE, FALSE), 1, 0)) AS CACHE_MISSES
FROM OVERWATCH_USAGE_LOG
WHERE LOG_TIME >= DATEADD('day', -7, CURRENT_TIMESTAMP())
GROUP BY PERF_RUN_ID, SECTION, EVENT_TYPE;

CREATE OR REPLACE VIEW PERF_TEST_SNOWFLAKE_QUERY_REPORT_V AS
WITH q AS (
    SELECT
        COALESCE(NULLIF(REGEXP_SUBSTR(QUERY_TAG, 'PERF:[A-Za-z0-9_.:-]+'), ''), 'PERF:UNLABELED') AS PERF_RUN_ID,
        QUERY_TAG,
        WAREHOUSE_NAME,
        USER_NAME,
        ROLE_NAME,
        DATABASE_NAME,
        EXECUTION_STATUS,
        TOTAL_ELAPSED_TIME,
        QUEUED_OVERLOAD_TIME,
        QUEUED_PROVISIONING_TIME,
        QUEUED_REPAIR_TIME,
        BYTES_SCANNED,
        BYTES_SPILLED_TO_REMOTE_STORAGE,
        PARTITIONS_SCANNED,
        PARTITIONS_TOTAL,
        PERCENTAGE_SCANNED_FROM_CACHE,
        START_TIME
    FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
    WHERE START_TIME >= DATEADD('day', -7, CURRENT_TIMESTAMP())
      AND QUERY_TAG ILIKE 'OVERWATCH%'
)
SELECT
    PERF_RUN_ID,
    WAREHOUSE_NAME,
    COUNT(*) AS QUERY_COUNT,
    SUM(IFF(EXECUTION_STATUS <> 'SUCCESS', 1, 0)) AS FAILED_QUERIES,
    ROUND(AVG(TOTAL_ELAPSED_TIME) / 1000, 2) AS AVG_ELAPSED_SEC,
    ROUND(PERCENTILE_CONT(0.95) WITHIN GROUP (ORDER BY TOTAL_ELAPSED_TIME) / 1000, 2) AS P95_ELAPSED_SEC,
    ROUND(SUM(COALESCE(QUEUED_OVERLOAD_TIME, 0) + COALESCE(QUEUED_PROVISIONING_TIME, 0) + COALESCE(QUEUED_REPAIR_TIME, 0)) / 1000, 2) AS QUEUED_SEC,
    ROUND(SUM(COALESCE(BYTES_SCANNED, 0)) / POWER(1024, 4), 3) AS TB_SCANNED,
    ROUND(SUM(COALESCE(BYTES_SPILLED_TO_REMOTE_STORAGE, 0)) / POWER(1024, 3), 2) AS REMOTE_SPILL_GB,
    ROUND(SUM(COALESCE(PARTITIONS_SCANNED, 0)) / NULLIF(SUM(COALESCE(PARTITIONS_TOTAL, 0)), 0) * 100, 2) AS PARTITION_SCAN_PCT,
    ROUND(AVG(PERCENTAGE_SCANNED_FROM_CACHE), 2) AS AVG_CACHE_PCT
FROM q
GROUP BY PERF_RUN_ID, WAREHOUSE_NAME
ORDER BY P95_ELAPSED_SEC DESC, TB_SCANNED DESC;

CREATE OR REPLACE VIEW PERF_TEST_COST_ESTIMATE_V AS
WITH qh AS (
    SELECT
        DATE_TRUNC('hour', START_TIME) AS HOUR_START,
        WAREHOUSE_NAME,
        COUNT(*) AS OVERWATCH_QUERIES
    FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
    WHERE START_TIME >= DATEADD('day', -7, CURRENT_TIMESTAMP())
      AND QUERY_TAG ILIKE 'OVERWATCH%'
    GROUP BY 1, 2
),
wh AS (
    SELECT
        DATE_TRUNC('hour', START_TIME) AS HOUR_START,
        WAREHOUSE_NAME,
        SUM(CREDITS_USED) AS CREDITS_USED
    FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSE_METERING_HISTORY
    WHERE START_TIME >= DATEADD('day', -7, CURRENT_TIMESTAMP())
    GROUP BY 1, 2
)
SELECT
    qh.WAREHOUSE_NAME,
    SUM(qh.OVERWATCH_QUERIES) AS OVERWATCH_QUERIES,
    ROUND(SUM(wh.CREDITS_USED), 3) AS SHARED_WAREHOUSE_CREDITS,
    ROUND(SUM(wh.CREDITS_USED) * 3.68, 2) AS SHARED_WAREHOUSE_DOLLARS,
    'Estimated: warehouse metering is shared when non-OVERWATCH queries ran in the same hour.' AS COST_CONFIDENCE
FROM qh
LEFT JOIN wh
  ON wh.HOUR_START = qh.HOUR_START
 AND UPPER(wh.WAREHOUSE_NAME) = UPPER(qh.WAREHOUSE_NAME)
GROUP BY qh.WAREHOUSE_NAME
ORDER BY SHARED_WAREHOUSE_CREDITS DESC;

CREATE OR REPLACE VIEW PERF_TEST_PRODUCTION_READINESS_V AS
WITH app AS (
    SELECT
        COALESCE(MAX(P95_DURATION_MS), 0) AS APP_P95_MS,
        COALESCE(MAX(MAX_DURATION_MS), 0) AS APP_MAX_MS
    FROM PERF_TEST_APP_USAGE_REPORT_V
),
sf AS (
    SELECT
        COALESCE(MAX(P95_ELAPSED_SEC), 0) AS QUERY_P95_SEC,
        COALESCE(SUM(FAILED_QUERIES), 0) AS FAILED_QUERIES,
        COALESCE(SUM(REMOTE_SPILL_GB), 0) AS REMOTE_SPILL_GB,
        COALESCE(MAX(PARTITION_SCAN_PCT), 0) AS PARTITION_SCAN_PCT
    FROM PERF_TEST_SNOWFLAKE_QUERY_REPORT_V
)
SELECT
    100
      - IFF(APP.APP_P95_MS > 5000, 20, IFF(APP.APP_P95_MS > 2500, 10, 0))
      - IFF(SF.QUERY_P95_SEC > 20, 20, IFF(SF.QUERY_P95_SEC > 8, 10, 0))
      - IFF(SF.FAILED_QUERIES > 0, 15, 0)
      - IFF(SF.REMOTE_SPILL_GB > 25, 15, IFF(SF.REMOTE_SPILL_GB > 5, 8, 0))
      - IFF(SF.PARTITION_SCAN_PCT > 80, 10, IFF(SF.PARTITION_SCAN_PCT > 50, 5, 0)) AS READINESS_SCORE,
    APP.APP_P95_MS,
    APP.APP_MAX_MS,
    SF.QUERY_P95_SEC,
    SF.FAILED_QUERIES,
    SF.REMOTE_SPILL_GB,
    SF.PARTITION_SCAN_PCT,
    CASE
        WHEN READINESS_SCORE >= 95 THEN 'PASS'
        WHEN READINESS_SCORE >= 85 THEN 'WATCH'
        ELSE 'FAIL'
    END AS READINESS_STATE
FROM app, sf;
