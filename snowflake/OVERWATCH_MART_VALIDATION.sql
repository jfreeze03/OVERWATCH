-- OVERWATCH_MART_VALIDATION.sql
-- Read-only deployment validation for the OVERWATCH command-center marts.
-- Run from the database/schema where OVERWATCH_MART_SETUP.sql was deployed.

-- 1) Required command-center objects.
WITH required_objects AS (
    SELECT * FROM VALUES
        ('TABLE', 'MART_EXECUTIVE_OBSERVABILITY'),
        ('TABLE', 'FACT_COST_DAILY'),
        ('TABLE', 'FACT_CORTEX_DAILY'),
        ('TABLE', 'FACT_QUERY_HOURLY'),
        ('TABLE', 'FACT_QUERY_DETAIL_RECENT'),
        ('TABLE', 'FACT_TASK_RUN'),
        ('TABLE', 'FACT_TASK_CRITICAL_PATH'),
        ('TABLE', 'FACT_PROCEDURE_RUN'),
        ('TABLE', 'ALERT_EVENTS'),
        ('TABLE', 'ALERT_ACKNOWLEDGEMENTS'),
        ('TABLE', 'ALERT_REMEDIATION_LOG'),
        ('TABLE', 'ALERT_NOTIFICATION_LOG'),
        ('TABLE', 'ALERT_THRESHOLDS'),
        ('TABLE', 'ALERT_OWNER_ROUTING'),
        ('TABLE', 'OVERWATCH_RECON_CONFIG'),
        ('TABLE', 'OVERWATCH_RECON_RUN'),
        ('TABLE', 'OVERWATCH_SCHEMA_DIFF_RESULT')
    AS t(REQUIRED_TYPE, REQUIRED_OBJECT)
),
existing_objects AS (
    SELECT TABLE_TYPE AS OBJECT_TYPE, TABLE_NAME AS OBJECT_NAME
    FROM INFORMATION_SCHEMA.TABLES
    WHERE TABLE_SCHEMA = CURRENT_SCHEMA()
    UNION ALL
    SELECT 'VIEW' AS OBJECT_TYPE, TABLE_NAME AS OBJECT_NAME
    FROM INFORMATION_SCHEMA.VIEWS
    WHERE TABLE_SCHEMA = CURRENT_SCHEMA()
)
SELECT
    r.REQUIRED_TYPE,
    r.REQUIRED_OBJECT,
    IFF(e.OBJECT_NAME IS NULL, 'MISSING', 'PRESENT') AS VALIDATION_STATUS
FROM required_objects r
LEFT JOIN existing_objects e
  ON UPPER(e.OBJECT_NAME) = r.REQUIRED_OBJECT
ORDER BY VALIDATION_STATUS DESC, REQUIRED_TYPE, REQUIRED_OBJECT;

-- 2) Deployable object count contract.
WITH expected_counts AS (
    SELECT * FROM VALUES
        ('TABLE', 56),
        ('VIEW', 2),
        ('PROCEDURE', 8),
        ('FUNCTION', 1)
    AS t(OBJECT_TYPE, EXPECTED_COUNT)
),
actual_counts AS (
    SELECT 'TABLE' AS OBJECT_TYPE, COUNT(*) AS ACTUAL_COUNT
    FROM INFORMATION_SCHEMA.TABLES
    WHERE TABLE_SCHEMA = CURRENT_SCHEMA()
      AND (
          TABLE_NAME ILIKE 'FACT\_%' ESCAPE '\'
          OR TABLE_NAME ILIKE 'DIM\_%' ESCAPE '\'
          OR TABLE_NAME ILIKE 'MART\_%' ESCAPE '\'
          OR TABLE_NAME ILIKE 'OVERWATCH\_%' ESCAPE '\'
          OR TABLE_NAME ILIKE 'ALERT\_%' ESCAPE '\'
      )
    UNION ALL
    SELECT 'VIEW', COUNT(*)
    FROM INFORMATION_SCHEMA.VIEWS
    WHERE TABLE_SCHEMA = CURRENT_SCHEMA()
      AND (
          TABLE_NAME ILIKE 'OVERWATCH\_%' ESCAPE '\'
          OR TABLE_NAME ILIKE 'ALERT\_%' ESCAPE '\'
      )
    UNION ALL
    SELECT 'PROCEDURE', COUNT(DISTINCT PROCEDURE_NAME)
    FROM INFORMATION_SCHEMA.PROCEDURES
    WHERE PROCEDURE_SCHEMA = CURRENT_SCHEMA()
      AND PROCEDURE_NAME ILIKE 'SP\_OVERWATCH\_%' ESCAPE '\'
    UNION ALL
    SELECT 'FUNCTION', COUNT(DISTINCT FUNCTION_NAME)
    FROM INFORMATION_SCHEMA.FUNCTIONS
    WHERE FUNCTION_SCHEMA = CURRENT_SCHEMA()
      AND FUNCTION_NAME ILIKE 'OVERWATCH\_%' ESCAPE '\'
)
SELECT
    e.OBJECT_TYPE,
    e.EXPECTED_COUNT,
    COALESCE(a.ACTUAL_COUNT, 0) AS ACTUAL_COUNT,
    IFF(COALESCE(a.ACTUAL_COUNT, 0) = e.EXPECTED_COUNT, 'PASS', 'FAIL') AS VALIDATION_STATUS
FROM expected_counts e
LEFT JOIN actual_counts a
  ON a.OBJECT_TYPE = e.OBJECT_TYPE
ORDER BY VALIDATION_STATUS DESC, e.OBJECT_TYPE;

-- 3) Task graph deployment proof.
SHOW TASKS IN SCHEMA;

WITH expected_tasks AS (
    SELECT * FROM VALUES
        ('OVERWATCH_ANOMALY_CHECK'),
        ('OVERWATCH_LOAD_HOURLY'),
        ('OVERWATCH_LOAD_CORTEX'),
        ('OVERWATCH_REFRESH_CONTROL_ROOM'),
        ('OVERWATCH_COST_MONITORING_REFRESH'),
        ('OVERWATCH_EXECUTIVE_OBSERVABILITY_REFRESH'),
        ('OVERWATCH_LOAD_DAILY')
    AS t(TASK_NAME)
),
actual_tasks AS (
    SELECT
        UPPER("name") AS TASK_NAME,
        UPPER(COALESCE("state", 'UNKNOWN')) AS TASK_STATE
    FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()))
)
SELECT
    e.TASK_NAME,
    COALESCE(a.TASK_STATE, 'MISSING') AS TASK_STATE,
    CASE
        WHEN a.TASK_NAME IS NULL THEN 'MISSING'
        WHEN a.TASK_STATE = 'SUSPENDED' THEN 'SUSPENDED'
        ELSE 'PRESENT'
    END AS VALIDATION_STATUS
FROM expected_tasks e
LEFT JOIN actual_tasks a
  ON a.TASK_NAME = e.TASK_NAME
ORDER BY VALIDATION_STATUS DESC, e.TASK_NAME;

-- 4) Executive board panel coverage.
WITH required_panels AS (
    SELECT * FROM VALUES
        ('KPI'),
        ('DAILY_COST'),
        ('MONTHLY_COST'),
        ('DAILY_WORKLOAD'),
        ('COST_DRIVER'),
        ('QUERY_TYPE'),
        ('QUERY_DATABASE'),
        ('EXEC_STATUS'),
        ('WAREHOUSE_PRESSURE'),
        ('FRESHNESS'),
        ('SOURCE_STATUS')
    AS t(PANEL)
),
actual_panels AS (
    SELECT PANEL, COUNT(*) AS ROW_COUNT, MAX(SNAPSHOT_TS) AS LATEST_SNAPSHOT
    FROM MART_EXECUTIVE_OBSERVABILITY
    GROUP BY PANEL
)
SELECT
    r.PANEL,
    COALESCE(a.ROW_COUNT, 0) AS ROW_COUNT,
    a.LATEST_SNAPSHOT,
    IFF(a.PANEL IS NULL, 'MISSING', 'PRESENT') AS VALIDATION_STATUS
FROM required_panels r
LEFT JOIN actual_panels a
  ON a.PANEL = r.PANEL
ORDER BY VALIDATION_STATUS DESC, r.PANEL;

-- 5) Executive board freshness by source.
SELECT
    COMPANY,
    ENVIRONMENT,
    WINDOW_DAYS,
    DIMENSION AS SOURCE_NAME,
    MAX(PERIOD_START) AS LATEST_SOURCE_TS,
    DATEDIFF('minute', MAX(PERIOD_START), CURRENT_TIMESTAMP()) AS AGE_MINUTES,
    MAX(SOURCE) AS SOURCE_TABLE
FROM MART_EXECUTIVE_OBSERVABILITY
WHERE PANEL = 'FRESHNESS'
GROUP BY COMPANY, ENVIRONMENT, WINDOW_DAYS, DIMENSION
ORDER BY AGE_MINUTES DESC NULLS FIRST, SOURCE_NAME;

